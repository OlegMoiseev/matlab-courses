[T2,X2] = ode45(@(t,x)(A*x + [0;sin(t)]),[0 15],x02);
[T3,X3] = ode45(@(t,x)(A*x + [0;sin(t)]),[0 15],x03);
[T4,X4] = ode45(@(t,x)(A*x + [0;sin(t)]),[0 15],x04);

figure(1)
plot(T,X(:,1),'r',T1,X1(:,1),'m',T2,X2(:,1),'c',...
    T3,X3(:,1),'b',T4,X4(:,1),'k'), grid on

